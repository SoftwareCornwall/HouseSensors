#ifndef STYLE_ESCAPE_SEQUENCES_DEFINED
#define STYLE_ESCAPE_SEQUENCES_DEFINED

// The following are escape sequences used to stylise the console:
#define DEFAULT_FONT "\033[0m"    // Resets the text to the default.
#define BOLD_FONT    "\033[1m"    // Makes the proceeding text bold.
#define GREEN_FONT   "\033[0;32m" //   ''   ''     ''      ''  green.
#define RED_FONT     "\033[0;31m" //   ''   ''     ''      ''  red.

#endif // STYLE_ESCAPE_SEQUENCES_DEFINED
